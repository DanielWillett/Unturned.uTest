        Type x0 = type.GetGenericArguments()[0];
        Assert.That(tokenizer.IsSameTypeAs(typeof(List<>).MakeGenericType(xx0)));
        Assert.That(tokenizer.IsSameTypeAs(typeof(Dictionary<,>).MakeGenericType(xx0, x0)));
    }

